# Gossip Project

This is a simple Sinatra application that allows users to create, view, edit, and update gossips. Gossips are stored in a CSV file, and each gossip has an author and content.

## Features

- Create new gossips
- View a list of all gossips
- Edit existing gossips
- Gossips are saved in a CSV file

